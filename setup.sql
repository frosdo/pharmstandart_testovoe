-- ============================================================
-- 1. Создание базы данных security_db
-- ============================================================
CREATE DATABASE security_db;

-- ============================================================
-- 2. Подключение к созданной БД
-- ============================================================
\c security_db;

-- ============================================================
-- 3. Создание таблицы auth_log
-- ============================================================
CREATE TABLE auth_log (
    id SERIAL PRIMARY KEY,
    username VARCHAR(50) NOT NULL,
    action VARCHAR(100) NOT NULL,
    login_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    ip_address INET,
    success BOOLEAN
);

-- ============================================================
-- 4. Создание роли auditor с паролем (соответствует политике)
-- ============================================================
CREATE ROLE auditor WITH LOGIN PASSWORD 'Farmstandart2026!';

-- ============================================================
-- 5. Назначение прав: только чтение таблицы auth_log
-- ============================================================
GRANT CONNECT ON DATABASE security_db TO auditor;
GRANT USAGE ON SCHEMA public TO auditor;
GRANT SELECT ON auth_log TO auditor;

